package com.honey.activity.stats;

import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;
import android.support.v4.view.ViewPager;
import android.view.Menu;

import com.honey.R;
import com.honey.activity.BaseFragmentActivity;

import java.util.Locale;

public class StatsActivity extends BaseFragmentActivity {
    private static final int MONTH_POSITION = 0;
    private static final int PAYEE_POSITION = 1;

    private MonthlyStatsFragment mMonthlyStatsFragment;

    SectionsPagerAdapter mSectionsPagerAdapter;
    ViewPager mViewPager;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_stats);

        mSectionsPagerAdapter = new SectionsPagerAdapter(getSupportFragmentManager());

        // Set up the ViewPager with the sections adapter.
        mViewPager = (ViewPager) findViewById(R.id.pager);
        mViewPager.setAdapter(mSectionsPagerAdapter);
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.stats, menu);
        return true;
    }


    /**
     * A {@link android.support.v4.app.FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            switch (position){
                case MONTH_POSITION:
                    mMonthlyStatsFragment = new MonthlyStatsFragment();
                    mMonthlyStatsFragment.setStatsType(MonthlyStatsFragment.StatsType.MONTHLY);
                    return mMonthlyStatsFragment;
                case PAYEE_POSITION:
                    mMonthlyStatsFragment = new MonthlyStatsFragment();
                    mMonthlyStatsFragment.setStatsType(MonthlyStatsFragment.StatsType.PAYEE);
                    return mMonthlyStatsFragment;
                default:
                    return null;
            }
        }

        @Override
        public int getCount() {
            return 2;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            Locale l = Locale.getDefault();
            switch (position) {
                case 0:
                    return getString(R.string.title_section1).toUpperCase(l);
                case 1:
                    return getString(R.string.title_section2).toUpperCase(l);
            }
            return null;
        }
    }
}
